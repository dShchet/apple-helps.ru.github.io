Цвет Space Gray был представлен три года назад одновременно с запуском iPhone 5s. Однако автор одного из таковых концептов под ником @appleidesigner, отметил, что хотя они и не демонстрируют действительного оттенка телефона, но близки к тому цвету, который будет использоваться в следующих смартфонах Apple.

Apple часто добавляет новые цвета в цветовую гамму собственных продуктов. И даже выпущенный в 2012 смартфон iPhone 5 нельзя было назвать темным в правильном понимании данного слова. Позже с выходом iPhone 5s производитель переименовал вариант White в Silver.

В Сеть уже попали и рендеры, воссозданные с учетом слухов о перемещении пластиковых вставок, изменения ободка у камеры и нового окраса. Кто окажется прав и как в действительности будет выглядеть новый смартфон компании Apple, мы навряд ли узнаем раньше до этого сентября.
