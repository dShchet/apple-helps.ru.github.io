Цвет будет намного ближе к черному, чем к серому. Как утверждается, наряду с новым цветом станут доступны старые варианты серебристый, золотистый и «розовое золото».

Бен Лавджой из 9to5Mac подтвердил эту информацию, отметив что Apple хочет ввести цвет Space Black. Эти сведения тогда показались несколько необычными, беря во внимание популярность данного оттенка среди уполномоченных сильного пола.

